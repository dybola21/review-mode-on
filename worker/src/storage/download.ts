import { Readable } from "node:stream";
import { pipeline } from "node:stream/promises";
import fs from "node:fs";
import path from "node:path";
import { spawn } from "node:child_process";
import { assertAllowedUrl, UrlAllowlistError } from "../security/url-allowlist.js";
import { safeBaseName, ensureInsideDir } from "./paths.js";
import type { InputFile } from "../types/contract.js";

export interface DownloadResult {
  localPath: string;
  bytes: number;
}

export class DownloadError extends Error {
  constructor(
    public readonly code: string,
    message: string,
  ) {
    super(message);
    this.name = "DownloadError";
  }
}

interface DownloadOptions {
  maxBytes: number;
  timeoutMs: number;
  allowedHosts: readonly string[];
  isProduction: boolean;
}

/**
 * Stream a signed input URL to a local file inside `destDir`. The output
 * name is generated by the worker (never the caller). Enforces:
 *  - allowed hostnames
 *  - HTTPS in production
 *  - byte limit (aborts the stream past the cap)
 *  - request timeout
 *  - single redirect via `redirect: "follow"` cap of 3 handled by fetch
 */
export async function downloadInput(
  input: InputFile,
  destDir: string,
  opts: DownloadOptions,
): Promise<DownloadResult> {
  let url: URL;
  try {
    url = assertAllowedUrl(input.signedUrl, opts.allowedHosts, "download", opts.isProduction);
  } catch (err) {
    if (err instanceof UrlAllowlistError) {
      throw new DownloadError(err.code, err.message);
    }
    throw new DownloadError("invalid_download_url", "URL de download inválida.");
  }

  // Worker-owned local name — never uses caller's fileName as a path.
  const ext = extForMime(input.mimeType) ?? path.extname(safeBaseName(input.fileName)) ?? "";
  const localName = `${input.fileType}_${input.fileId}${ext}`;
  const dest = ensureInsideDir(destDir, localName);
  fs.mkdirSync(path.dirname(dest), { recursive: true });

  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), opts.timeoutMs);

  let res: Response;
  try {
    res = await fetch(url, { redirect: "follow", signal: controller.signal });
  } catch (err) {
    clearTimeout(timer);
    if ((err as { name?: string })?.name === "AbortError") {
      throw new DownloadError("input_download_timeout", "Timeout ao baixar arquivo.");
    }
    throw new DownloadError("input_download_failed", "Falha ao baixar arquivo.");
  }
  if (!res.ok || !res.body) {
    clearTimeout(timer);
    if (res.status === 401 || res.status === 403) {
      throw new DownloadError("input_expired", "URL de download expirada.");
    }
    if ([429, 500, 502, 503, 504].includes(res.status)) {
      throw new DownloadError("input_download_transient", "Falha temporária ao baixar arquivo.");
    }
    throw new DownloadError("input_download_failed", "Falha ao baixar arquivo.");
  }

  let bytes = 0;
  const readable = Readable.fromWeb(res.body as never);
  const outStream = fs.createWriteStream(dest);
  try {
    await pipeline(async function* () {
      for await (const chunk of readable) {
        const buf = chunk as Buffer;
        bytes += buf.length;
        if (bytes > opts.maxBytes) {
          controller.abort();
          throw new DownloadError("input_too_large", "Arquivo excede tamanho máximo permitido.");
        }
        yield buf;
      }
    }, outStream);
  } catch (err) {
    clearTimeout(timer);
    try {
      fs.unlinkSync(dest);
    } catch {
      /* noop */
    }
    if (err instanceof DownloadError) throw err;
    throw new DownloadError("input_download_failed", "Falha ao baixar arquivo.");
  } finally {
    clearTimeout(timer);
  }

  if (bytes === 0) {
    try {
      fs.unlinkSync(dest);
    } catch {
      /* noop */
    }
    throw new DownloadError("input_empty", "Arquivo vazio.");
  }

  return { localPath: dest, bytes };
}

function extForMime(mime: string): string | null {
  switch ((mime || "").toLowerCase()) {
    case "video/mp4":
      return ".mp4";
    case "video/quicktime":
      return ".mov";
    case "video/webm":
      return ".webm";
    case "audio/mpeg":
      return ".mp3";
    case "audio/mp4":
      return ".m4a";
    case "audio/wav":
      return ".wav";
    case "image/png":
      return ".png";
    case "image/jpeg":
      return ".jpg";
    case "image/webp":
      return ".webp";
    case "image/svg+xml":
      return ".svg";
    default:
      return null;
  }
}

