export interface ProbeInfo {
  format: string;
  durationSeconds: number;
  width?: number;
  height?: number;
  hasAudio: boolean;
  hasVideo: boolean;
  videoCodec?: string;
  audioCodec?: string;
}

export async function ffprobe(file: string, ffprobeTimeoutMs = 15_000): Promise<ProbeInfo> {
  return new Promise((resolve, reject) => {
    const args = ["-v", "error", "-print_format", "json", "-show_format", "-show_streams", file];
    const child = spawn("ffprobe", args, { shell: false });
    let out = "";
    let err = "";
    const t = setTimeout(() => {
      child.kill("SIGKILL");
      reject(new DownloadError("ffprobe_timeout", "ffprobe demorou demais."));
    }, ffprobeTimeoutMs);
    child.stdout.on("data", (d) => (out += d.toString()));
    child.stderr.on("data", (d) => (err = (err + d.toString()).slice(-4096)));
    child.on("error", () => {
      clearTimeout(t);
      reject(new DownloadError("ffprobe_failed", "Falha ao inspecionar arquivo."));
    });
    child.on("close", (code) => {
      clearTimeout(t);
      if (code !== 0) {
        return reject(new DownloadError("ffprobe_failed", "Arquivo inválido."));
      }
      try {
        const parsed = JSON.parse(out) as {
          format?: { format_name?: string; duration?: string };
          streams?: Array<{
            codec_type?: string;
            codec_name?: string;
            width?: number;
            height?: number;
          }>;
        };
        const streams = parsed.streams ?? [];
        const v = streams.find((s) => s.codec_type === "video");
        const a = streams.find((s) => s.codec_type === "audio");
        resolve({
          format: parsed.format?.format_name ?? "",
          durationSeconds: Number(parsed.format?.duration ?? 0) || 0,
          width: v?.width,
          height: v?.height,
          hasAudio: Boolean(a),
          hasVideo: Boolean(v),
          videoCodec: v?.codec_name,
          audioCodec: a?.codec_name,
        });
      } catch {
        reject(new DownloadError("ffprobe_failed", "Saída ffprobe inválida."));
      }
    });
  });
}

/**
 * Confirm the downloaded file's content roughly matches the declared MIME.
 * We rely on ffprobe rather than parsing raw bytes.
 */
export function assertMimeMatchesProbe(mime: string, info: ProbeInfo): void {
  const m = (mime || "").toLowerCase();
  if (m.startsWith("video/") && !info.hasVideo) {
    throw new DownloadError("mime_mismatch", "Arquivo declarado como vídeo não contém vídeo.");
  }
  if (m.startsWith("audio/") && !info.hasAudio) {
    throw new DownloadError("mime_mismatch", "Arquivo declarado como áudio não contém áudio.");
  }
  if (m.startsWith("image/") && !info.hasVideo) {
    // ffprobe reports single image as a video stream, so hasVideo should be true.
    throw new DownloadError("mime_mismatch", "Arquivo de imagem inválido.");
  }
}
